#include<stdio.h>

////////////////////////////////////////////////////////////////////////////
//
//  Function Name :    KMtoMeter
//  Description   :    Given distance Number from user and disaplay convert   
//                     KiloMeter to Meter.
//  Input         :    Integer
//  Output        :    Integer
//  Author        :    Satyajeet Manohar Ghule
//  Date          :    26/10/2025
//
////////////////////////////////////////////////////////////////////////////

int KMtoMeter(int iNo)
{
    int Meter = 0;

    Meter = iNo * 1000;
    return Meter;
}
////////////////////////////////////////////////////////////////////////////
//
//  Entry point Function for the Application
//
////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0, iRet = 0;

    printf("Enter a Number :\n");
    scanf("%d",&iValue);

    iRet= KMtoMeter(iValue);
    
    printf("%d KiloMeter Equal to : %d Meter",iValue, iRet);
    return 0; 
}
