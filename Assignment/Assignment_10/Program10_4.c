////////////////////////////////////////////////////////////////////////////
//
//  Function Name :    FhtoCs
//  Description   :    Given a Number from user and disaplay    
//                     Tempretur in celsius.
//  Input         :    Float
//  Output        :    Double
//  Author        :    Satyajeet Manohar Ghule
//  Date          :    27/10/2025
