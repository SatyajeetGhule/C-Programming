double FhtoCs(float iNo)
{
    double celsius = 0.0f;
    celsius = (iNo - 32) * (5.0 / 9.0);
    return celsius;
}
////////////////////////////////////////////////////////////////////////////
//
//  Entry point Function for the Application
//
////////////////////////////////////////////////////////////////////////////

int main()
{
    float iValue = 0.0f;
    double dRet = 0.0f;

    printf("Enter Tempreture in Fahrenheit :\n");
    scanf("%f",&iValue);

    dRet = FhtoCs(iValue);
    printf("Tempretur in Celsius :%f \n",dRet);

    return 0;
}
