#include<stdio.h>

////////////////////////////////////////////////////////////////////////////
//
//  Function Name :    Dispaly
//  Description   :    Given number from user and print numbers from  
//                     negative to positive numbers.
//  Input         :    Integer
//  Output        :    Integer
//  Author        :    Satyajeet Manohar Ghule
//  Date          :    25/10/2025
//
////////////////////////////////////////////////////////////////////////////
void Display(int iNo)
{
    int iCnt = 0;

    for( iCnt = -iNo; iCnt <= iNo; iCnt++)
    {
        printf("%d\t",iCnt);
    }
}
////////////////////////////////////////////////////////////////////////////
//
//  Entry point Function for the Application
//
////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;
    printf("Enter a number:\n");
    scanf("%d",&iValue);

    Display(iValue);
    return 0;
}
