#include<stdio.h>

////////////////////////////////////////////////////////////////////////////
//
//  Function Name :    Pattern
//  Description   :    Given number from user and print on Screen $ and *
//  Input         :    Integer, Integer, Integer
//  Output        :    Integee
//  Author        :    Satyajeet Manohar Ghule
//  Date          :    25/10/2025
//
////////////////////////////////////////////////////////////////////////////

void Pattern(int iNo)
{
    int iCnt = 0;
    if(iNo <= 0)
    {
        iNo = -iNo;
    }
    for(iCnt = 1; iCnt<=iNo; iCnt++)
    {
        printf("$  *  ");
    }
}
////////////////////////////////////////////////////////////////////////////
//
//  Entry point Function for the Application
//
////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter a number :\n");
    scanf("%d",&iValue);

    Pattern(iValue);

    return 0;
}
