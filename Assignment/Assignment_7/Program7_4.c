#include<stdio.h>
////////////////////////////////////////////////////////////////////////////
//
//  Function Name :    OddDisplay
//  Description   :    Given a number from user and Display all Odd numbers 
//                     up to N.
//  Input         :    Integer
//  Output        :    Integer
//  Author        :    Satyajeet Manohar Ghule
//  Date          :    25/10/2025
//
////////////////////////////////////////////////////////////////////////////
void OddDisaplay(int iNo)
{
    int iCnt = 0;

    for(iCnt = 2; iCnt < iNo; iCnt++)
    {
        if(iCnt % 2 != 0)
        {
            printf("%d\t",iCnt);
        }
    }
}

////////////////////////////////////////////////////////////////////////////
//
//  Entry point Function for the Application
//
////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;

    printf("Enter a number :\n");
    scanf("%d",&iValue);

    OddDisaplay(iValue);
    return 0;
}
