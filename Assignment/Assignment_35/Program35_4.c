#include<stdio.h>

////////////////////////////////////////////////////////////////////////////
//
//  Function Name :    Fact
//  Description   :    Accept number from user and return its factorial.
//  Input         :    Integer
//  Output        :    Integer
//  Author        :    Satyajeet Manohar Ghule
//  Date          :    10/12/2025
//
////////////////////////////////////////////////////////////////////////////

int Fact(int iNo)
{
    if( iNo == 0 || iNo == 1)
    {
        return 1;
    }
    return iNo * Fact(iNo - 1);
}

////////////////////////////////////////////////////////////////////////////
//
//  Entry point Function for the Application
//
////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;
    printf("Enter a number :\n");
    scanf("%d",&iValue);
    iRet = Fact(iValue);
    printf("Factorial value is : %d\n",iRet);
    return 0;
}

