int Sum(int iNo)
{
    int iDigit = 0;
    if(iNo == 0)

    return 0;

    iDigit = iNo % 10;

    return iDigit + Sum(iNo / 10);

}

////////////////////////////////////////////////////////////////////////////
//
//  Entry point Function for the Application
//
////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;
    printf("Enter a Number :\n");
    scanf("%d",&iValue);
    iRet = Sum(iValue);
    printf("Summation of Digit is : %d\n",iRet);
    return 0;
}

