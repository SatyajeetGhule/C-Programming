void Display(int iNo)
{
    if(iNo == 0)
    return;    
    printf("%d\t",iNo);   
    Display(iNo -1);
}

////////////////////////////////////////////////////////////////////////////
//
//  Entry point Function for the Application
//
////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    printf("Enter a Number :\n");
    scanf("%d",&iValue);
    Display(iValue);
    return 0;
}

