void Display(int iNo, char ch)
{
    if(iNo == 0)
    return;
    
    printf("%c\t",ch);
   
    ch++;
    Display(iNo -1, ch);
}

////////////////////////////////////////////////////////////////////////////
//
//  Entry point Function for the Application
//
////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    printf("Enter a Number :\n");
    scanf("%d",&iValue);
    Display(iValue, 'A');
    return 0;
}

