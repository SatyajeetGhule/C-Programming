#include<stdio.h>

////////////////////////////////////////////////////////////////////////////
//
//  Function Name :    Display
//  Description   :    Accept number from user and Display this pattern( * * * * *).
//  Input         :    Integer
//  Output        :    Void (Print String)
//  Author        :    Satyajeet Manohar Ghule
//  Date          :    10/12/2025
//
////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    if(iNo == 0)
    return;
    
    printf("*\t");
       
    Display(iNo -1);
}

////////////////////////////////////////////////////////////////////////////
//
//  Entry point Function for the Application
//
////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    printf("Enter a Number :\n");
    scanf("%d",&iValue);
    Display(iValue);
    return 0;
}

