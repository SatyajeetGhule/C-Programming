void Display(int iNo)
{
    if( iNo == 0)
    return;
    Display(iNo - 1);
    printf("%d\t",iNo);

}

////////////////////////////////////////////////////////////////////////////
//
//  Entry point Function for the Application
//
////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    printf("Enter a Number :\n");
    scanf("%d",&iValue);
    Display(iValue);
    return 0;
}

