void Pattern(int iNo)
{
    for(char cCnt= 'A'; cCnt <= ('A'+iNo)-1; cCnt++)
    {
        printf("%c\t",cCnt);
    }
    printf("\n");
}

////////////////////////////////////////////////////////////////////////////
//
//  Entry point Function for the Application
//
////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter a Number of Frequency :\n");
    scanf("%d",&iValue);

    Pattern(iValue);
    return 0;
}

