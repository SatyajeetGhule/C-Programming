#include <stdio.h>
////////////////////////////////////////////////////////////////////////////
//
//  Function Name :    Pattern
//  Description   :    Accept Number from user and disaplay table of number
//                     2 and print up to N number .
//  Input         :    Integer
//  Output        :    Integer
//  Author        :    Satyajeet Manohar Ghule
//  Date          :    21/11/2025
//
////////////////////////////////////////////////////////////////////////////

void Pattern(int iNo)
{
    int ans = 0;
    for(int iCnt = 1; iCnt <= iNo; iCnt++)
    {
        ans = iCnt * 2;
        printf("%d\t",ans);
    }
    printf("\n");
}

////////////////////////////////////////////////////////////////////////////
//
//  Entry point Function for the Application
//
////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter a Number of Frequency :\n");
    scanf("%d",&iValue);

    Pattern(iValue);
    return 0;
}

