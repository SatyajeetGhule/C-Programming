void DisplayDigit(char *str)
{
    while(*str != '\0')
    {        
        if(*str >= '0' && *str <= '9')
        {
            printf("%c",*str);
        }
        *str++;
    }
}

////////////////////////////////////////////////////////////////////////////
//
//  Entry point Function for the Application
//
////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[20];
    
    printf("Enter a String :\n");
    scanf("%[^'\n']s",Arr);

    DisplayDigit(Arr);

    //printf("Update String : %s",Arr);
    return 0;
}

