//  Function Name :    DisplayDigit
//  Description   :    accept string from user and display only digits
//                     from that string.
//  Input         :    String
//  Output        :    void (print String)
//  Author        :    Satyajeet Manohar Ghule
//  Date          :    29/11/2025
