void struprX(char *str)
{
    while(*str != '\0')
    {        
        if(*str >= 'a' && *str <= 'z')
        {
            *str = *str - 32;
        }
        *str++;
    }
}

////////////////////////////////////////////////////////////////////////////
//
//  Entry point Function for the Application
//
////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[20];
    
    printf("Enter a String :\n");
    scanf("%[^'\n']s",Arr);

    struprX(Arr);

    printf("Converted into Uppar Case : %s",Arr);
    return 0;
}

